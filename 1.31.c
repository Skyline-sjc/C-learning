void YH_triangle(int n){
    int i = 0, j = 0;
    int temp = 2;
    int a[10] = {0};
    int b[10] = {0};
    a[0] = 1;
    b[0] = 1;
    b[1] = 1;
    j = 0;
    while (temp <= n+1)
    {
        j = 0;
        while(a[j]!=0){
            printf("%d ", a[j++]);
        }
        printf("\n");
        a[0] = 1;
        a[temp - 1] = 1;
        for (j = 1; j < temp - 1;j++){
            a[j] = b[j - 1] + b[j];
        }
        for (j = 0; j < 10;j++){
            b[j] = a[j];
        }
        temp++;
    }
}
